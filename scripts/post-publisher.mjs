// Publisher — writes the MDX file, commits it, and pushes to main so Vercel
// picks it up. In DRY_RUN mode it stops after writing the file locally.

import { access, writeFile } from 'node:fs/promises'
import { join } from 'node:path'
import { execSync } from 'node:child_process'

const BLOG_DIR = 'content/blog'

// Escape any " in user-controlled fields so the YAML frontmatter stays valid.
function escapeYaml(value) {
  return String(value).replace(/"/g, '\\"')
}

async function fileExists(path) {
  try {
    await access(path)
    return true
  } catch {
    return false
  }
}

function run(cmd) {
  return execSync(cmd, { encoding: 'utf-8' }).trim()
}

export async function publishPost(post, { dryRun = false } = {}) {
  const date = new Date().toISOString().split('T')[0]
  const slug = post.slug
  const filePath = join(BLOG_DIR, `${slug}.mdx`)

  // Guard against overwriting an existing post — a slug collision usually
  // means the topic was already covered, or a human edited a draft.
  if (await fileExists(filePath)) {
    throw new Error(`File already exists: ${filePath}. Refusing to overwrite.`)
  }

  const mdx = `---
title: "${escapeYaml(post.title)}"
date: "${date}"
excerpt: "${escapeYaml(post.excerpt)}"
category: "${post.category}"
readTime: "${escapeYaml(post.readTime)}"
slug: "${slug}"
targetKeyword: "${escapeYaml(post.targetKeyword)}"
coverImage: "yash-makeovers/blog/${slug}"
published: true
autoGenerated: true
---

${post.body}
`

  await writeFile(filePath, mdx, 'utf-8')
  console.log(`📝 Wrote: ${filePath}`)

  const url = `https://yashmakeovers.com/blog/${slug}`

  if (dryRun) {
    return { slug, url, filePath }
  }

  // Git config — only needed in CI (locally these are already set by the user)
  try {
    run('git config user.email "blog-engine@yashmakeovers.com"')
    run('git config user.name "Yash Makeovers Blog Engine"')
  } catch {
    // Ignore — likely fine if running locally with global git config.
  }

  // Sync with remote in case the cron raced with a manual push, then commit + push.
  try {
    run('git pull --rebase origin main')
  } catch (err) {
    console.warn('⚠️  git pull --rebase failed, continuing anyway:', err?.message ?? err)
  }
  run(`git add "${filePath}"`)
  run(`git commit -m "blog(auto): ${post.title.replace(/"/g, '\\"')}"`)
  run('git push origin main')

  console.log('🚀 Pushed to GitHub — Vercel deploying...')

  return { slug, url, filePath }
}
